public class IfElsePractice {

    public static void main(String[] args) {
        int n = 0;

        if (n < 20) {
            if (n > 0) {
                System.out.println("0");
            }
            System.out.println("1");
        } else {
            System.out.println("2");
        }

        System.out.println("3");
    }
}
